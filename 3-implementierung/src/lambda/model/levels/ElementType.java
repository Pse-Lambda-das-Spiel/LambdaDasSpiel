package lambda.model.levels;
/**
 * Enumeration of the different element-types
 *
 * @author Kay Schmitteckert
 */
public enum ElementType {
    /**
     * Represents variable
     */
    VARIABLE,
    /**
     * Represents abstraction
     */
    ABSTRACTION,
    /**
     * Represents paranthesis
     */
    PARANTHESIS
}
